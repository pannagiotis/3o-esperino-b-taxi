 <?php
  /* Αυτός ο κώδικας είναι για να δείξουμε 
 τη λειτουργία του while: 
 
 Ουσιαστικά παίρνει ένα όριο μέχρι και τυπώνει όλους τους αριθμούς έως εκεί.. 
 */
 
 
 $mexri = $_GET['mexri'];
 
$i = 0;

while($i < $mexri){
	echo $i.'<br />';
	$i = $i + 1;     // για συντομία αυτό γράφεται και ως εξής $i += 1 ή ακόμα και $i++
}

?>